export function getAppointmentById(id: string): Appointment | undefined {
  return appointments.find((appointment) => appointment.id === id);
}

export function getAppointmentsByPhone(phone: string): Appointment[] {
  return appointments
    .filter((appointment) => appointment.customerPhone === phone)
    .sort((a, b) => new Date(b.startAt).getTime() - new Date(a.startAt).getTime());
}

export function getAppointmentsByStaffId(staffId: string): Appointment[] {
  return appointments
    .filter((appointment) => appointment.staffId === staffId)
    .sort((a, b) => new Date(a.startAt).getTime() - new Date(b.startAt).getTime());
}
